package chapter04;

public class StringTest01 {
	public static void main(String[] args) {
		System.out.println("c:\\temp");
		
		// c:\temp
		// \t : tab
		// \r : carriage return
		// \n : newline
		// \b : beep
		
		System.out.print("hello\r\n");
		
		// "hello"
		System.out.println("\"hello\"");
		
		// '
		char c = '\'';
		System.out.println(c);
		
	}

}
